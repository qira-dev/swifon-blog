interface LanguageState {
  langCode: string;
  setLangCode: (code: string) => void;
}

export const useLanguage = create<LanguageState>((set) => ({
  langCode: localStorage.getItem("qirahub_lang") || "en",
  setLangCode: (code: string) => {
    localStorage.setItem("qirahub_lang", code);
    set({ langCode: code });
    if (code === "ar") {
      document.documentElement.dir = "rtl";
    } else {
      document.documentElement.dir = "ltr";
    }
  },
}));

export function t(key: TranslationKey, lang?: string): string {
  const code = lang || useLanguage.getState().langCode;
  return translations[code]?.[key] || translations["en"][key] || key;
}

export function useTranslation() {
  const { langCode } = useLanguage();
  return {
    t: (key: TranslationKey) => translations[langCode]?.[key] || translations["en"][key] || key,
    langCode,
  };
}
